class vehiculo {
    constructor(id, marca, modelo, año, color, tipo, transmision, combustible, precio, financiamiento, estadoVehiculo, seguroVehicular) {
        this.id = id;
        this.marca = marca;
        this.modelo = modelo;
        this.año = año;
        this.color = color;
        this.tipo = tipo;
        this.transmision = transmision;
        this.combustible = combustible;
        this.precio = precio;
        this.financiamiento = financiamiento;
        this.estadoVehiculo = estadoVehiculo;
        this.seguroVehicular = seguroVehicular;
    }
}
